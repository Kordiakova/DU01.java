public class WhileCyklus {
    public static void main(String[] args) {

        int cislo = 0;

        while (cislo < 6){
            System.out.println(cislo);
            cislo++;
        }

        int cislo2 = 10;

        while (cislo2 > 0){
            System.out.println(cislo2);
            cislo2--;
        }
    }

}
